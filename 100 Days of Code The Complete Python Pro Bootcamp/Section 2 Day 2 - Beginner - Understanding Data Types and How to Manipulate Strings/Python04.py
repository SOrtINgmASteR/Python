# Python Operators

a = 22
b = 5
print(f"a = {a}\nb = {b}")

summation = a + b
subtraction = a - b
multiplication = a * b
division = a / b
exponent = a**b
integer_division = a // b
modulus = a % b

print(f"a + b : {summation}")
print(f"a - b : {subtraction}")
print(f"a * b : {multiplication}")
print(f"a / b : {division}")
print(f"a ** b : {exponent}")
print(f"a // b : {integer_division}")
print(f"a % b : {modulus}")
