# Math Functions

import math
print(round(3.458922, 2))
print("Ceiling(3.45) :", math.ceil(3.45))
print("Flooring(3.45) :", math.floor(3.45))
print("GCD(10, 15, 30, 35) :", math.gcd(10, 15, 30, 35))
print("LCM(10, 15, 30, 35) :", math.lcm(10, 15, 30, 35))
print("Factorial(8) :", math.factorial(8))
print("Square_root(81) :", math.sqrt(81))
print("Cube_root(125) :", math.cbrt(125))
print("5 power 3 :", math.pow(5, 3))


