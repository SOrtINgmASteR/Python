# Python Lists

fruits = ["mangoes", "bananas", "lychees", "grapes", "watermelons", "jackfruit"]
my_numbers = [25, 45, 1, 5, 8, 3, 11, 9]

print(fruits)
print(fruits[0], end=" ")
print(fruits[1], end=" ")
print(fruits[2], end=" ")
print(fruits[-1], end=" ")
print(fruits[-2], end=" ")
print(fruits[-3])

print(my_numbers)
print(my_numbers[0], end=" ")
print(my_numbers[1], end=" ")
print(my_numbers[2], end=" ")
print(my_numbers[-1], end=" ")
print(my_numbers[-2], end=" ")
print(my_numbers[-3], end=" ")
print(my_numbers[-4])

